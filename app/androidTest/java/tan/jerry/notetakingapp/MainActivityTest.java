package com.example.notetakingapp;

import androidx.test.ext.junit.runners.AndroidJUnit4;
import androidx.test.rule.ActivityTestRule;
import org.junit.Rule;
import org.junit.Test;
import org.junit.runner.RunWith;
import static androidx.test.espresso.Espresso.onView;
import static androidx.test.espresso.action.ViewActions.click;
import static androidx.test.espresso.action.ViewActions.typeText;
import static androidx.test.espresso.matcher.ViewMatchers.withId;
import static androidx.test.espresso.assertion.ViewAssertions.matches;
import static androidx.test.espresso.matcher.ViewMatchers.withText;

@RunWith(AndroidJUnit4.class)
public class MainActivityTest {

    @Rule
    public ActivityTestRule<MainActivity> activityRule =
            new ActivityTestRule<>(MainActivity.class);

    @Test
    public void testNoteTaking() {
        // Type text into the EditText field
        onView(withId(R.id.noteEditText)).perform(typeText("Test Note"));

        // Click the save button
        onView(withId(R.id.saveButton)).perform(click());

        // Verify the note appears in the TextView
        onView(withId(R.id.savedNoteTextView)).check(matches(withText("Test Note")));
    }
}
